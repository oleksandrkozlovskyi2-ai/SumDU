# введення a
a = int(input("Введіть a: "))
while a <= 0:   # тільки додатні числа
    a = int(input("Введіть a (додатнє число): "))

# введення b
b = int(input("Введіть b: "))
while b <= 0:   # тільки додатні числа
    b = int(input("Введіть b (додатнє число): "))

# обчислення X
if a > b:
    X = 5 * a + b
elif a == b:
    X = -125
else:   # a < b
    X = (a - 5) / b

print("Результат обчислення X:", X)
