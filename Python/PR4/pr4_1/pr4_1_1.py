# Дано одномірний масив, що складається з N цілочисельних елементів.
# Масив користувач має ввести з клавіатури. Вивести додатні елементи.

n = int(input("Введіть кількість елементів масиву n = "))

print(f"Введіть {n} елементів масиву:")
arr = [int(input()) for _ in range(n)]

print("Початковий масив:", arr)

# Виведення лише додатних елементів
positive = [x for x in arr if x > 0]

print("Додатні елементи масиву:", positive)
