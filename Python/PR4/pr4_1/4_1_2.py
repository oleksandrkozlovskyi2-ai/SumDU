# Створити двовимірний масив 7x7 і заповнити його, як на рисунку.

n = 7  # розмірність масиву

# створюємо 2D масив
a = [[0] * n for _ in range(n)]

# заповнення циклом
for i in range(n):
    for j in range(n):
        a[i][j] = n - i   # значення залежить тільки від рядка

# вивід масиву
for row in a:
    print(*row)
