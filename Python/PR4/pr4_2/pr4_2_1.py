# Доповнення списку з обох кінців

def extend_list():
    # Введення списку з клавіатури
    A = list(map(int, input("Введіть список чисел через пробіл: ").split()))
    print("Початковий список:", A)

    # Елемент для додавання на початок і кінець
    left = int(input("Введіть число для додавання на початок: "))
    right = int(input("Введіть число для додавання в кінець: "))

    # Доповнення списку
    A.insert(0, left)     # додати зліва
    A.append(right)       # додати справа

    print("Список після доповнення:", A)
    return A

# Виклик функції
extend_list()
